#include <iostream>
using namespace std;
int dp[2][3][2], arr[2][3];
int main() {
	int n; cin >> n;
	for(int i=0; i<n; i++){
		i%2==0 ? cin >> arr[0][0] >> arr[0][1] >> arr[0][2] : cin >> arr[1][0] >> arr[1][1] >> arr[1][2];
		if(i==0){
			dp[0][0][0]=arr[0][0];
			dp[0][0][1]=arr[0][0];
			dp[0][1][0]=arr[0][1];
			dp[0][1][1]=arr[0][1];
			dp[0][2][0]=arr[0][2];
			dp[0][2][1]=arr[0][2];
		}
		else{
			if(i%2!=0){
				dp[1][0][0]=max(dp[0][0][0], dp[0][1][0])+arr[1][0];
				dp[1][1][0]=max(dp[0][0][0], max(dp[0][1][0], dp[0][2][0]))+arr[1][1];
				dp[1][2][0]=max(dp[0][1][0], dp[0][2][0])+arr[1][2];
				dp[1][0][1]=min(dp[0][0][1], dp[0][1][1])+arr[1][0];
				dp[1][1][1]=min(dp[0][0][1], min(dp[0][1][1], dp[0][2][1]))+arr[1][1];
				dp[1][2][1]=min(dp[0][1][1], dp[0][2][1])+arr[1][2];
			}
			else{
				dp[0][0][0]=max(dp[1][0][0], dp[1][1][0])+arr[0][0];
				dp[0][1][0]=max(dp[1][0][0], max(dp[1][1][0], dp[1][2][0]))+arr[0][1];
				dp[0][2][0]=max(dp[1][1][0], dp[1][2][0])+arr[0][2];
				dp[0][0][1]=min(dp[1][0][1], dp[1][1][1])+arr[0][0];
				dp[0][1][1]=min(dp[1][0][1], min(dp[1][1][1], dp[1][2][1]))+arr[0][1];
				dp[0][2][1]=min(dp[1][1][1], dp[1][2][1])+arr[0][2];
			}
		}
	}
	int m=-1, s=2147483647;
	if(n%2){
		m=max(dp[0][0][0], max(dp[0][1][0], dp[0][2][0]));
		s=min(dp[0][0][1], min(dp[0][1][1], dp[0][2][1]));
	}
	else{
		m=max(dp[1][0][0], max(dp[1][1][0], dp[1][2][0]));
		s=min(dp[1][0][1], min(dp[1][1][1], dp[1][2][1]));
	}
	cout << m << " " << s;
}